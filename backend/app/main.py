from imports import (
    FastAPI, CORSMiddleware, auth
)

app = FastAPI(title="Sweet Shop Management System", version="1.0.0")

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

app.include_router(auth.router)